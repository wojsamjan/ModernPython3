days_of_week = ("Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday")

# count, index
sunday_count = days_of_week.count("Sunday")
monday_count = days_of_week.count("Monday")
sunday_index = days_of_week.index("Sunday")

print(days_of_week)
print(f"Sunday count: {sunday_count}")
print(f"Monday count: {monday_count}")
print(f"Sunday index: {sunday_index}")
